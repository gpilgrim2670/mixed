#' Area of a trapezoid
#'
#' Calculates the area of a trapezoid
#'
#' @author Greg Pilgrim \email{gpilgrim2670@@gmail.com}
#'
#' @param a the length of one trapezoid face
#' @param b the length of the other trapezoid face
#' @param h the height of the trapezoid
#' @return the area of a trapezoid defined by \code{a}, \code{b} and \code{h}

trap_area <- function(a, b, h){
  area = (a + b)*h/2
}
